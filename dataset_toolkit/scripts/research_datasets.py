def write_reports(rows: list[dict[str, str]]) -> None:
    ensure_dir(REPORTS_DIR)
    research_lines = [
        "# Dataset Research Report",
        "",
        "This report is generated from a manually curated research list. Downloaders use only official Git, Kaggle, Roboflow, or direct URLs and never bypass authentication or license gates.",
        "",
    ]
    license_lines = ["# Licenses and Citations", ""]
    for row in rows:
        research_lines.extend(
            [
                f"## {row['dataset_name']}",
                "",
                f"- Dataset ID: `{row['dataset_id']}`",
                f"- Source: {row['source_url']}",
                f"- Status: `{row['status']}`",
                f"- Download method: `{row['download_method']}`",
                f"- Annotation format: {row['annotation_format']}",
                f"- Classes: {row['classes_available']}",
                f"- Useful for: {row['useful_for']}",
                f"- Notes: {row['notes']}",
                "",
            ]
        )
        license_lines.extend(
            [
                f"## {row['dataset_name']}",
                "",
                f"- Source: {row['source_url']}",
                f"- License / terms: {row['license']}",
                f"- Citation: {row['citation']}",
                "",
            ]
        )
    write_text(REPORTS_DIR / "dataset_research_report.md", "\n".join(research_lines))
    write_text(REPORTS_DIR / "licenses_and_citations.md", "\n".join(license_lines))


def main() -> None:
    write_manifest(DATASETS)
